    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()
                || jTextField3.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Data Pelanggan Belum di isi", "Gagal Tambah Data", 2); //2 adalah WARNING MESSAGE
            }else{
                String queryTambah = "insert into pelanggan values (null, ?, ?, ?);";
                pst = conn.prepareStatement(queryTambah);
                pst.setString(1, jTextField1.getText());
                pst.setString(2, jTextField2.getText());
                pst.setString(3, jTextField3.getText());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Pelanggan Berhasil Ditambahkan", "Sukses", 1);
                tampilData();
                bersih();
            }
        } catch (SQLException ex) {
            Logger.getLogger(PelangganView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

