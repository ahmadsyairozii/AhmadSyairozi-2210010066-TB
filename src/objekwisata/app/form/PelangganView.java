    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            if(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()
                || jTextField3.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Data Pelanggan Belum di isi", "Gagal Tambah Data", 2); //2 adalah WARNING MESSAGE
            }else{
                int konfirmasi = JOptionPane.showConfirmDialog(null, "Hapus Data Pelanggan ini?", "Konfirmasi Hapus?", 0);
                if(konfirmasi == 0){
                    String queryTambah = "delete from pelanggan where id=? ";
                    pst = conn.prepareStatement(queryTambah);
                    pst.setString(1, id);
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data Pelanggan Berhasil Dihapus", "Sukses", 1);
                    tampilData();
                    bersih();
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(PelangganView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

